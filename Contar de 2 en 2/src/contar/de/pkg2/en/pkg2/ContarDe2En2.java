package contar.de.pkg2.en.pkg2;

public class ContarDe2En2 {

    /*
    realizar un programa que muestre por pantalla los numeros
    del 200 al 250 santando de 2 en 2
     */
    public static void main(String[] args) {

        int num = 200;
        while (num <= 250) {
            System.out.println(num);
            num = num + 2;
        }
    }

}
